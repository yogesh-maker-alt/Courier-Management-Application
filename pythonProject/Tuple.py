



my_tuple = (1,1,2,4,5,6,7)

#   Gives Count of Occurrence of element    #
print(my_tuple.count(1))

#   Gives Index of Element  #
print(my_tuple.index(4))

#   Arranges Tuple Elements in Ascending Order   #
print(sorted(my_tuple))

#   Arranges Tuple Elements in Descending Order   #
print(sorted(my_tuple, reverse=True))

print(min(my_tuple))

print(max(my_tuple))
